
public class Main {

	public static void main(String[] args) {
		System.out.println("Hello World");
		int x;
		double y;
		boolean z;
		float a;
		long b;
		
		x = 2; 					//-2billions - 2 billions
		y = 3.14;				//Takes up to 7 precision digits
		z = true;				//True / False
		a = 3.14f; 				//needs a f
		b = 1353252453523L;		//9 Quintillions - 9 Quintillions
		
		System.out.println("Int Value: " + x);
		System.out.println("Double Value: " + y);
		System.out.println("Boolean Value: " + z);
		System.out.println("Float Value: " + a);
		System.out.println("Long Value: " + b);
	}

}
